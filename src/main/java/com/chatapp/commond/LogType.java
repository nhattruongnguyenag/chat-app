package com.chatapp.commond;

/**
 * Định nghĩa các hằng số cho các loại log.
 *
 * Các loại log bao gồm:
 * - LOG: Loại log thông thường.
 * - WARNING: Loại log cảnh báo.
 * - ERROR: Loại log lỗi.
 * - EXCEPTION: Loại log ngoại lệ.
 * - SUCCESS: Loại log thành công.
 */
public class LogType {
    /**
     * Loại log thông thường.
     */
    public static final String LOG = "LOG";

    /**
     * Loại log cảnh báo.
     */
    public static final String WARNING = "WARNING";

    /**
     * Loại log lỗi.
     */
    public static final String ERROR = "ERROR";

    /**
     * Loại log ngoại lệ.
     */
    public static final String EXCEPTION = "EXCEPTION";

    /**
     * Loại log thành công.
     */
    public static final String SUCCESS = "SUCCESS";
}
